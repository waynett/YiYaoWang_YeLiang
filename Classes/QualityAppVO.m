#import "QualityAppVO.h"


@implementation QualityAppVO

@synthesize appName;
@synthesize appLinkUrl;
@synthesize appExplain;
@synthesize appIconUrl;

-(void)dealloc{
	if (appName != nil) {
		[appName release];
		appName = nil;
	}
	if (appLinkUrl != nil) {
		[appLinkUrl release];
		appLinkUrl = nil;
	}
	if (appExplain != nil) {
		[appExplain release];
		appExplain = nil;
	}
	if (appIconUrl != nil) {
		[appIconUrl release];
		appIconUrl = nil;
	}
	[super dealloc];
}

@end
