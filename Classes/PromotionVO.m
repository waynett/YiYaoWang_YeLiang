#import "PromotionVO.h"


@implementation PromotionVO

@synthesize conditions;
@synthesize description;
@synthesize detailUrl;
@synthesize endTime;
@synthesize imageUrl1;
@synthesize imageUrl2;    
@synthesize name;
@synthesize startTime;
@synthesize topicId;

-(void)dealloc{
    if(conditions!=nil){
        [conditions release];
    }
    if(description!=nil){
        [description release];
    }
    if(detailUrl!=nil){
        [detailUrl release];
    }
    if(endTime!=nil){
        [endTime release];
    }
    if(imageUrl1!=nil){
        [imageUrl1 release];
    }
    if(imageUrl2!=nil){
        [imageUrl2 release];
    }
    if(name!=nil){
        [name release];
    }
    if(startTime!=nil){
        [startTime release];
    }
    if(topicId!=nil){
        [topicId release];
    }
    [super dealloc];
}

@end
