#import "CheckSecKillResult.h"

@implementation CheckSecKillResult
@synthesize ifSecKill;
@synthesize startDate;
@synthesize endDate;
@synthesize canSecKill;

-(void)dealloc
{
    if (ifSecKill!=nil) {
        [ifSecKill release];
        ifSecKill=nil;
    }
    if (startDate!=nil) {
        [startDate release];
        startDate=nil;
    }
    if (endDate!=nil) {
        [endDate release];
        endDate=nil;
    }
    if (canSecKill!=nil) {
        [canSecKill release];
        canSecKill=nil;
    }
    
    [super dealloc];
}
@end
