#import "CheckValidCodeForPhoneRegisterResult.h"

@implementation CheckValidCodeForPhoneRegisterResult
@synthesize resultCode,errorInfo;

-(void)dealloc
{
    if (resultCode!=nil) {
        [resultCode release];
        resultCode=nil;
    }
    if (errorInfo!=nil) {
        [errorInfo release];
        errorInfo=nil;
    }
    [super dealloc];
}
@end
