#import "CountyVO.h"


@implementation CountyVO

@synthesize countyName;
@synthesize nid;
@synthesize postcode;

-(void)dealloc{
    if(countyName!=nil){
        [countyName release];
    }
    if(nid!=nil){
        [nid release];
    }
    if(postcode!=nil){
        [postcode release];
    }
    [super dealloc];
}

@end
