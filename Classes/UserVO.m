#import "UserVO.h"


@implementation UserVO

@synthesize amount;
@synthesize boughtTimes;
@synthesize cocode;
@synthesize couponCount;
@synthesize email;
@synthesize messageCount;
@synthesize pictureUrl;
@synthesize points;
@synthesize userId;
@synthesize userMobile;
@synthesize username;
@synthesize userPhone;
@synthesize vip;
@synthesize cardAmount;
@synthesize availableAmount;
@synthesize frozenAmount;
@synthesize userRealName;
@synthesize availableCardAmount;
@synthesize totalAmount;
@synthesize cardNum;
@synthesize frozenCardAmount;
@synthesize enduserPoint;
-(void)dealloc{
    if (amount!=nil) {
        [amount release];
    }
    if (boughtTimes!=nil) {
        [boughtTimes release];
    }
    if (cocode!=nil) {
        [cocode release];
    }
    if (couponCount!=nil) {
        [couponCount release];
    }
    if (email!=nil) {
        [email release];
    }
    if (messageCount!=nil) {
        [messageCount release];
    }
    if (pictureUrl!=nil) {
        [pictureUrl release];
    }
    if (points!=nil) {
        [points release];
    }
    if (userId!=nil) {
        [userId release];
    }
    if (userMobile!=nil) {
        [userMobile release];
    }
    if (username!=nil) {
        [username release];
    }
    if (userPhone!=nil) {
        [userPhone release];
    }
    if (vip!=nil) {
        [vip release];
    }
    if (cardAmount!=nil) {
        [cardAmount release];
    }
    if (availableAmount!=nil) {
        [availableAmount release];
        availableAmount=nil;
    }
    if (frozenAmount!=nil) {
        [frozenAmount release];
        frozenAmount=nil;
    }
    if (userRealName!=nil) {
        [userRealName release];
        userRealName=nil;
    }
    OTS_SAFE_RELEASE(availableCardAmount);
    OTS_SAFE_RELEASE(cardNum);
    OTS_SAFE_RELEASE(totalAmount);
    OTS_SAFE_RELEASE(frozenCardAmount);
    OTS_SAFE_RELEASE(enduserPoint);
    [super dealloc];
}

@end
